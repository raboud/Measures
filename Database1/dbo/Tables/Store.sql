CREATE TABLE [dbo].[Store] (
    [Id]                  INT            IDENTITY (1, 1) NOT NULL,
    [TypeID]              INT            NOT NULL,
    [Number]              NVARCHAR (50)  NULL,
    [BillingAddress]           NVARCHAR (255) NULL,
    [City]                     NVARCHAR (50)  NULL,
    [State]                    NVARCHAR (20)  NULL,
    [ZipCode]                  NVARCHAR (20)  NULL,
    [StorePhoneNumber]         NVARCHAR (30)  NULL,
    [DirectPhoneNumber]        NVARCHAR (30)  NULL,
    [Extension]                NVARCHAR (30)  NULL,
    [FaxNumber]                NVARCHAR (30)  NULL,
    [Notes]                    NTEXT          NULL,
    [BranchId]                 INT            NOT NULL,
    [Active]                   BIT            CONSTRAINT [DF__Store__Active] DEFAULT ((1)) NOT NULL,
    [StoreNickName]            NVARCHAR (50)  CONSTRAINT [DF__Store__StoreNickName] DEFAULT (NULL) NULL,
    [Latitude]                 FLOAT (53)     NULL,
    [Longitude]                FLOAT (53)     NULL,
    [DistrictNumber]             SMALLINT       NULL,
    [IncludeInStatusReportAll] BIT            CONSTRAINT [DF_Store_IncludeInStatusReportAll] DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PK_Store] PRIMARY KEY NONCLUSTERED ([Id] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_Store_Branch] FOREIGN KEY ([BranchId]) REFERENCES [dbo].[Branch] ([Id]),
    CONSTRAINT [FK_Store_StoreType] FOREIGN KEY ([TypeID]) REFERENCES [dbo].[StoreType] ([Id]), 
);

